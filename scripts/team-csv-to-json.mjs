#!/usr/bin/env node
/**
 * Converts a Google Sheets CSV export into config/team.json.
 *
 * Expected columns (header row, any order):
 *   id, displayName, operatingLevel, skill_1, skill_2, skill_3, skill_4, skill_5
 *
 * Skips:
 *   - Empty rows
 *   - Rows whose first cell starts with # (comments / instructions)
 *
 * Usage:
 *   node scripts/team-csv-to-json.mjs ./path/to/Roster.csv
 *   npm run team:import -- ./path/to/Roster.csv
 *
 * Default input: config/team-import.csv
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

function parseCSV(text) {
  const rows = [];
  let row = [];
  let field = "";
  let inQuotes = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    const next = text[i + 1];
    if (c === '"') {
      if (inQuotes && next === '"') {
        field += '"';
        i++;
      } else {
        inQuotes = !inQuotes;
      }
    } else if (!inQuotes && c === ",") {
      row.push(field);
      field = "";
    } else if (!inQuotes && (c === "\n" || c === "\r")) {
      if (c === "\r" && next === "\n") i++;
      row.push(field);
      rows.push(row);
      row = [];
      field = "";
    } else {
      field += c;
    }
  }
  row.push(field);
  if (row.some((cell) => String(cell).length > 0)) {
    rows.push(row);
  }
  return rows;
}

function norm(s) {
  return String(s ?? "")
    .replace(/^\uFEFF/, "")
    .trim();
}

function findHeaderIndex(headerRow) {
  const lower = headerRow.map((h) => norm(h).toLowerCase());
  const need = [
    "id",
    "displayname",
    "operatinglevel",
    "skill_1",
    "skill_2",
    "skill_3",
    "skill_4",
    "skill_5",
  ];
  const idx = {};
  for (const name of need) {
    const i = lower.indexOf(name);
    if (i === -1) {
      throw new Error(
        `Missing column "${name}". Required: id, displayName, operatingLevel, skill_1 … skill_5`,
      );
    }
    const key =
      name === "displayname"
        ? "displayName"
        : name === "operatinglevel"
          ? "operatingLevel"
          : name;
    idx[key] = i;
  }
  return idx;
}

function main() {
  const argPath = process.argv[2];
  const inputPath = path.resolve(
    root,
    argPath || "config/team-import.csv",
  );

  if (!fs.existsSync(inputPath)) {
    console.error(`File not found: ${inputPath}`);
    console.error(
      "Export your Google Sheet as CSV, then run:\n  npm run team:import -- ./YourExport.csv",
    );
    process.exit(1);
  }

  const raw = fs.readFileSync(inputPath, "utf8");
  const table = parseCSV(raw).map((r) => r.map(norm));

  if (table.length === 0) {
    console.error("CSV is empty.");
    process.exit(1);
  }

  const col = findHeaderIndex(table[0]);
  const members = [];
  const seen = new Set();

  for (let r = 1; r < table.length; r++) {
    const line = table[r];
    const idCell = line[col.id] ?? "";
    if (!idCell || idCell.startsWith("#")) continue;
    const id = idCell.trim();
    const displayName = line[col.displayName] ?? "";
    if (!displayName) {
      console.warn(`Skipping row ${r + 1}: missing displayName for id "${id}"`);
      continue;
    }

    const operatingLevel = (line[col.operatingLevel] ?? "").trim();
    if (!operatingLevel) {
      throw new Error(
        `Row ${r + 1} (${id}): operatingLevel is required (e.g. Senior, Staff).`,
      );
    }

    const skills = [];
    for (let k = 1; k <= 5; k++) {
      const key = `skill_${k}`;
      const label = line[col[key]] ?? "";
      if (!label.trim()) {
        throw new Error(
          `Row ${r + 1} (${id}): "${key}" is empty — each teammate needs five skill labels.`,
        );
      }
      skills.push({ key, label: label.trim() });
    }

    if (seen.has(id)) {
      throw new Error(`Duplicate id "${id}" — each teammate must have a unique id.`);
    }
    seen.add(id);

    members.push({
      id,
      displayName: displayName.trim(),
      operatingLevel,
      skills,
    });
  }

  if (members.length === 0) {
    console.error(
      "No data rows found. Remove instruction rows or ensure each row has id + displayName + five skills.",
    );
    process.exit(1);
  }

  const out = {
    $comment:
      "Generated by npm run team:import — edit roster in Google Sheet and re-export, or edit this file.",
    members,
  };

  const outPath = path.join(root, "config", "team.json");
  fs.writeFileSync(outPath, `${JSON.stringify(out, null, 2)}\n`, "utf8");

  console.log(`Wrote ${members.length} teammate(s) to ${outPath}`);
}

main();
